<p>
	On the afternoon of September 9, 1947, the Harvard Mark II electromechanical computer began to
	misbehave. The operators traced the fault to relay panel F, position 70, where they found a
	dead moth wedged between the contacts. They removed the moth. The Mark II resumed correct
	operation. Someone, almost certainly William "Bill" Burke rather than Grace Hopper, taped the
	moth into the engineering logbook with the wry annotation: <em>"First actual case of bug being
	found."</em>
</p>

<p>
	The page is now in the Smithsonian's National Museum of American History. It is the most
	frequently-cited primary source in the etymology of computing's most-used English noun. It also
	does not say what people think it says.
</p>

<p>
	"Bug," meaning an unintended defect in a piece of machinery, was a term of art among electrical
	engineers for at least seventy years before the Mark II incident. Thomas Edison complained
	about "bugs" in his telegraph equipment in an 1878 letter. Quadruplex telegraph operators
	called the small artifacts that disrupted their signaling "bugs." Telephone-exchange engineers
	called intermittent faults "bugs." By the 1920s, the word was in routine use throughout the
	electrical industry. The Mark II operators in 1947 were not coining a term. They were making a
	joke.
</p>

<p>
	The joke is good, and it survived for the same reason all good jokes do: it sits on a perfect
	hinge. "Bug" had been a metaphor for decades. A literal bug, an actual insect, an
	identifiable lepidopteran specimen, in the actual mechanism, is the moment the metaphor briefly
	collapses into a physical referent. The page in the logbook is the punchline of an engineering
	tradition that was already half a century old when the moth flew into the relay.
</p>

<p>
	Grace Hopper's role in the popular telling is overstated. She was on the Mark II team but did
	not, by her own later accounts, personally extract the moth. She did, however, retell the story
	for the rest of her career, and she was a beloved enough speaker that the story has become
	hers in the public memory. The logbook is signed by Burke. The handwriting on the
	"First actual case" caption is debated. Hopper herself was characteristically forthcoming about
	the embellishment; she liked the story, used it as a teaching device, and was rarely careful
	about correcting the attribution.
</p>

<p>
	The lesson is small and worth saying. <em>Etymology is recovery</em>. Words come from somewhere.
	When the source is a tradition, the words tend to be older than the famous moment everyone
	associates them with. The Mark II moth is the most famous moth in computing, and it is also a
	postcard from a tradition that already had a word for what the moth did.
</p>

<p>
	The moth is in good condition for an eighty-year-old specimen, taped flat under glass. It is
	smaller than you expect.
</p>
