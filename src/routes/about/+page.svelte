	<h2 class="!mt-2 font-display text-3xl font-semibold text-ink-900">About this guide</h2>

	<p>
		Bugs is a field guide to twelve specimens collected from the history of computing between
		1947 and 2024. Each entry is illustrated, dated, taxonomized, and described in the voice of
		someone who finds the particular causes interesting and the consequences instructive. The
		project takes its visual cue from naturalist field guides, which is the form best suited to
		things that are at once technical, peculiar, and worth a closer look.
	</p>

	<p>
		The selection is curatorial. The criteria, loosely held: the bug had to be real and
		well-documented; the cause had to be specific and explicable in a paragraph; the consequences
		had to be either instructive, funny, or both. Aerospace tragedies sit next to PostScript
		printing failures. A moth sits next to a missing overbar. I did not attempt to be
		comprehensive. I attempted to be good.
	</p>

	<p>
		Each diagram is drawn from scratch as inline SVG. Nothing is generated, nothing is borrowed.
		The illustrations exist because the bug stories are usually told without them, and a good
		drawing is occasionally more honest than a paragraph.
	</p>

	<p>
		Sources for every claim are linked at the foot of each entry. If you find an error, the
		repository is on <a href="https://github.com/ma-r-s/bugs">GitHub</a> and pull requests are
		welcome, though the catalog itself is curated rather than open.
	</p>

